"""Benchmark reporter — generates Markdown reports with tables and query plans."""

from __future__ import annotations

from collections import defaultdict
from datetime import datetime, timezone
from typing import Any

from .runner import BenchmarkResult


def generate_report(
    results: list[BenchmarkResult],
    title: str = "SalesOS Benchmark Report",
) -> str:
    """Generate a complete Markdown benchmark report."""
    lines: list[str] = []

    lines.append(f"# {title}")
    lines.append("")
    lines.append(f"**Date:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S UTC')}")
    lines.append("")
    lines.append("---")
    lines.append("")

    # Group by dataset size
    by_dataset: dict[int, list[BenchmarkResult]] = defaultdict(list)
    for r in results:
        by_dataset[r.dataset_size].append(r)

    for dataset_size in sorted(by_dataset.keys()):
        lines.append(f"## Dataset: {dataset_size:,} Companies")
        lines.append("")

        dataset_results = by_dataset[dataset_size]

        # Summary table
        lines.append("### Performance Summary")
        lines.append("")
        lines.append("| Query | Category | Rows | Min (ms) | p50 (ms) | p95 (ms) | p99 (ms) | Avg (ms) | Max (ms) |")
        lines.append("|-------|----------|-----:|---------:|---------:|---------:|---------:|---------:|---------:|")

        for r in sorted(dataset_results, key=lambda x: x.p95_ms, reverse=True):
            lines.append(
                f"| {r.name} | {r.category} | {r.row_count} | "
                f"{r.min_ms:.2f} | {r.p50_ms:.2f} | {r.p95_ms:.2f} | "
                f"{r.p99_ms:.2f} | {r.avg_ms:.2f} | {r.max_ms:.2f} |"
            )

        lines.append("")

        # Category breakdown
        lines.append("### By Category")
        lines.append("")
        by_cat: dict[str, list[BenchmarkResult]] = defaultdict(list)
        for r in dataset_results:
            by_cat[r.category].append(r)

        for cat in ["exact", "partial", "filter", "sort", "pagination", "count"]:
            if cat not in by_cat:
                continue
            cat_results = by_cat[cat]
            p95_vals = [r.p95_ms for r in cat_results]
            avg_p95 = sum(p95_vals) / len(p95_vals) if p95_vals else 0
            max_p95 = max(p95_vals) if p95_vals else 0
            lines.append(f"- **{cat.title()}** ({len(cat_results)} queries): p95 avg={avg_p95:.2f}ms, p95 max={max_p95:.2f}ms")

        lines.append("")

        # EXPLAIN ANALYZE
        lines.append("### Query Plans (EXPLAIN ANALYZE)")
        lines.append("")
        for r in dataset_results:
            if r.explain_output:
                lines.append(f"<details>")
                lines.append(f"<summary>{r.name} — {r.p95_ms:.2f}ms p95</summary>")
                lines.append("")
                lines.append("```")
                lines.append(r.explain_output)
                lines.append("```")
                lines.append("")
                lines.append("</details>")
                lines.append("")

    # Overall ranking of slowest queries
    lines.append("## Top 10 Slowest Queries (p95)")
    lines.append("")
    lines.append("| Query | Dataset | p95 (ms) | Category |")
    lines.append("|-------|--------:|---------:|----------|")
    all_sorted = sorted(results, key=lambda x: x.p95_ms, reverse=True)[:10]
    for r in all_sorted:
        lines.append(f"| {r.name} | {r.dataset_size:,} | {r.p95_ms:.2f} | {r.category} |")

    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("*Report generated by SalesOS Benchmark Framework*")

    return "\n".join(lines)


def generate_json_report(results: list[BenchmarkResult]) -> dict[str, Any]:
    """Generate a JSON-serializable report."""
    by_dataset: dict[int, list[dict]] = defaultdict(list)
    for r in results:
        by_dataset[r.dataset_size].append({
            "name": r.name,
            "description": r.description,
            "category": r.category,
            "dataset_size": r.dataset_size,
            "row_count": r.row_count,
            "min_ms": round(r.min_ms, 2),
            "p50_ms": round(r.p50_ms, 2),
            "p95_ms": round(r.p95_ms, 2),
            "p99_ms": round(r.p99_ms, 2),
            "avg_ms": round(r.avg_ms, 2),
            "max_ms": round(r.max_ms, 2),
            "durations_ms": [round(d, 2) for d in r.durations_ms],
            "explain_output": r.explain_output,
        })

    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "datasets": {str(k): v for k, v in sorted(by_dataset.items())},
    }
